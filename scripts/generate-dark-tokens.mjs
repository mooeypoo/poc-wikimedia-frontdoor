/**
 * generate-dark-tokens.mjs — regenerate app/assets/css/color-modes.css.
 *
 * Codex ships dark-mode token values as a Less mixin (`.cdx-mode-dark()`) under
 * an unscoped `:root` in its plain-CSS build, so neither can be consumed
 * directly for a runtime toggle. This script lifts the mixin's declaration
 * body and rescopes it under the first-party theme classes the shell applies to
 * <html> (see app/composables/useColorMode.ts).
 *
 * We generate rather than hand-copy so the token values stay exact against the
 * installed Codex version. Re-run after bumping @wikimedia/codex-design-tokens,
 * verify the diff, then commit:
 *
 *   node scripts/generate-dark-tokens.mjs
 */

import { readFileSync, writeFileSync } from 'node:fs'
import { fileURLToPath } from 'node:url'
import { dirname, join } from 'node:path'
import { createRequire } from 'node:module'

const require = createRequire( import.meta.url )
const repoRoot = join( dirname( fileURLToPath( import.meta.url ) ), '..' )

const mixinPath = require.resolve(
	'@wikimedia/codex-design-tokens/theme-wikimedia-ui-mixin-dark.less'
)
const pkg = require( '@wikimedia/codex-design-tokens/package.json' )
const outPath = join( repoRoot, 'app/assets/css/color-modes.css' )

const src = readFileSync( mixinPath, 'utf8' )

// Extract the declaration body inside `.cdx-mode-dark() { ... }`.
const body = src.slice( src.indexOf( '{' ) + 1, src.lastIndexOf( '}' ) ).trim()

/**
 * Re-indents declaration lines to the given tab depth.
 *
 * @param {string} block - Raw declaration block.
 * @param {number} depth - Number of leading tabs per non-empty line.
 * @return {string} Re-indented block.
 */
function indent( block, depth ) {
	const pad = '\t'.repeat( depth )
	return block
		.split( '\n' )
		.map( ( line ) => ( line.trim() ? pad + line.trim() : '' ) )
		.join( '\n' )
}

const out = `/**
 * color-modes.css — dark-mode design tokens for the Front Door shell.
 *
 * GENERATED by scripts/generate-dark-tokens.mjs — do not edit by hand.
 * These are the Codex dark-mode token values (\`.cdx-mode-dark()\`) scoped under
 * first-party theme classes so the whole shell, content, and explorer follow
 * the user's color-mode choice.
 *
 * The light theme is the default \`:root\` set imported in main.css; this file
 * only re-declares the tokens that differ in dark mode.
 *
 * Color-mode choice is applied as a class on <html> by useColorMode.ts:
 *   .fd-theme--dark  → always dark
 *   .fd-theme--auto  → dark only when the OS prefers dark (pure CSS, no JS)
 *   .fd-theme--light → light (falls through to :root defaults)
 *
 * REGENERATE after bumping @wikimedia/codex-design-tokens:
 *   node scripts/generate-dark-tokens.mjs
 * Source: codex-design-tokens v${ pkg.version }, theme-wikimedia-ui-mixin-dark.less.
 */

html.fd-theme--dark {
	color-scheme: dark;
${ indent( body, 1 ) }
}

@media ( prefers-color-scheme: dark ) {
	html.fd-theme--auto {
		color-scheme: dark;
${ indent( body, 2 ) }
	}
}
`

writeFileSync( outPath, out )
process.stdout.write(
	`Wrote ${ outPath } from codex-design-tokens v${ pkg.version }\n`
)
